# Функция для перевода величин в стандартный вид [ug.mL-1]
def convert_to_ug_mL(concentration):
    conversion_factors = {
        'uM': 1e-6,
        'mm': 1e-3,
        'mg/dL': 1e-3,
        'mg/L': 1e-6,
        'IU/L': 1e-6,
        'g': 1e-6,
        'u/L': 1e-6,
        'CELLS/uL': 1e-6,
        'g/dL': 1e-6,
        'meq/L': 1e-6,
        'ug/L': 1e-6,
        'mM': 1e-3,
        'umol/L': 1e-6,
        'nM': 1e-9,
        'pg': 1e-12,
        'mg/ml': 1e-3,
        'ug': 1e-6,
        'uM/ml': 1e-6,
        'mmol/L': 1e-3,
        'mg/kg': 1e-3,
        'microM': 1e-6,
        'nmol': 1e-9,
        'mg': 1e-3,
        'g/ml': 1e-3,
        'ug/mL': 1e-6,
    }

    if concentration in conversion_factors:
        # Конвертируем в ug.mL-1
        return 1 / conversion_factors[concentration]  # возвращаем коэффициент, чтобы выразить в ug.mL-1
    return None

# Конвертация всех величин в ug.mL-1
standard_values = {unit: convert_to_ug_mL(unit) for unit in concentration_units}
standard_values = {k: v for k, v in standard_values.items() if v is not None}

# Фильтрация неконцентрированных величин
non_concentration_units = [
    'hr', 's', '/s', 'cm', '/100WBC', 'day', 'fl', 'min', 'g', 'mL',
    'uL/hr/cm2', 'CFU', 'U/min', 'log10CFU', 'pg/ml', 'uL/ml', 'ml',
    'g/dL', 'log10CFU/g', '10\'-5 ug/ml', 'weeks'
]

# Вывод результатов
print("Концентрации в ug.mL-1:")
for original, standard in standard_values.items():
    print(f"{original}: {standard:.2f} ug.mL-1")

print("\nНеконцентрированные величины:")
for non_concentration in non_concentration_units:
    print(non_concentration)

