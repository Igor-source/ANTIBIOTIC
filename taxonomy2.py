def get_tax_id(bacterial_name):
    """Fetches the Taxonomy ID for a given bacterial name."""
    try:
        search_handle = Entrez.esearch(db="taxonomy", term=bacterial_name)
        search_results = Entrez.read(search_handle)
        search_handle.close()

        # Get the first Taxonomy ID from the search results
        if search_results["IdList"]:
            return search_results["IdList"][0]
        else:
            print(f"No Taxonomy ID found for {bacterial_name}")
            return None
    except Exception as e:
        print(f"Error searching for {bacterial_name}: {e}")
        return None

def get_superkingdom_from_name(bacterial_name):
    """Fetches the superkingdom for a given bacterial name."""
    tax_id = get_tax_id(bacterial_name)
    if tax_id:
        try:
            handle = Entrez.efetch(db="taxonomy", id=tax_id, retmode="xml")
            record = Entrez.read(handle)
            handle.close()

            if record:
                for lineage in record[0]['LineageEx']:
                    if lineage['Rank'] == 'superkingdom':
                        return lineage['ScientificName']
            return None
        except Exception as e:
            print(f"Error fetching superkingdom for Taxonomy ID {tax_id}: {e}")
            return None
    return None

